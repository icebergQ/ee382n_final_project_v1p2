package ee382n;

public class Point2D {
    public double X;
    public double Y;
    public Point2D(double x, double y)
    {
        X = x;
        Y = y;
    }

    public double getX() {
        return X;
    }

    public double getY() {
        return Y;
    }

    public void setX(double x) {
        X = x;
    }

    public void setY(double y) {
        Y = y;
    }
}
